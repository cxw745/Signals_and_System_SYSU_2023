clear all;
close all;
clc;
fs=1000;
t=-1.1:1/fs:2.1;
x1=t.*(t>=0);
x2=exp(-1*t).*(t>=0);
y=conv(double(x1),double(x2))/fs;
len=length(y);
tt=(0:len-1)/fs-2.2;
subplot(2,2,1);
plot(t,x1);
grid on;
title('x1');
subplot(2,2,2);
plot(t,x2);
grid on;
title('x2');
subplot(2,1,2);
plot(tt,y);
grid on;
title('conv(x1,x2)');