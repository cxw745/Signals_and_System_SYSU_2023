clear all;
close all;
clc;
x=[1 2 1 1 0 -3];
k1=[0,1,2,3,4,5];
h=[1 -1 1];
k2=[0,1,2];
y=conv(x,h);
subplot(2,2,1);
stem(k1,x);
title('x');
xlabel('k1');
grid on;
subplot(2,2,2);
stem(k2,h);
title('h');
xlabel('k2');
grid on;
subplot(2,1,2);
stem([0:length(y)-1],y);
title('y[k]');
xlabel('k');
grid on;