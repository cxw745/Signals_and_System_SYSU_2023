close all;
clear all;
clc;
syms m k n z;
xn= 0.5^n;
Xz = ztrans(xn,n,z);
Hz=(2*z^2-z)/(2*z^2-z-3);
Yzs = Xz * Hz ;
yzsn = iztrans(Yzs,z,n)
n=[-10:10];
yzsn=subs(yzsn,'n',n);
figure(1);
subplot(3,1,1);
stem(n,yzsn,'filled');
xlabel('n');
ylabel('yzs(n)');
title('Zero State Response');
grid on;
Yin=(10*z^2-3*z)/(2*z^2-z-3);
yinn=iztrans(Yin,z,m);
m=[-10:10];
yinn=subs(yinn,'m',m);
subplot(3,1,2);
stem(m,yinn,'filled');
xlabel('n');
ylabel('yin(n)');
title('Zero Input Response');
grid on;
Yz=Yin+Yzs;
yn=iztrans(Yz,z,k);
k=[-10:10];
yn=subs(yn,'k',k);
subplot(3,1,3);
stem(k,yn,'filled');
xlabel('n');
ylabel('y(n)');
title('Complete Response');
