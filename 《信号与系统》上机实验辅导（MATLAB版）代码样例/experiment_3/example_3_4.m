clear all;
close all;
clc;
syms t w;
x1=heaviside(t+0.25)-heaviside(t-0.25);
Xw1=simple(fourier(x1));
subplot(3,1,1);
ezplot(abs(Xw1),[-24*pi 24*pi]);
grid on;
x2=cos(8*pi*t)*x1;
Xw2=simple(fourier(x2));
subplot(3,1,2);
ezplot(abs(Xw2),[-24*pi 24*pi]);
grid on;
Xf2=subs(Xw2,'w','2*pi*f');
subplot(3,1,3);
ezplot(abs(Xf2),[-12 12]);
grid on;