clear all;
close all;
clc;
b=[-1 0 4];
a=[0 4];
[h w]=freqs(b,a,100);
Magnitude=abs(h);
Phase=angle(h)*180/pi;
figure(1);
subplot(2,1,1);
plot(w,Magnitude);
xlabel('w');
ylabel('Magnitude');
grid on;
subplot(2,1,2);
plot(w,Phase);
xlabel('w');
ylabel('Phase');
grid on;
figure(2);
syms t w;
ht=ifourier(4/(4+w^2),t);
subplot(2,1,1);
ezplot(ht);
title('h(t)')
grid on;
w=-6:0.1:6;
Xw=4./(4+w.^2);
subplot(2,1,2);
plot(w,Xw);
xlabel('w');
title('H(w)');
grid on;