r=0.01;
t=-5:r:5;
f1=heaviside(t+pi)-heaviside(t-pi);
f2=heaviside(t+pi)-2*heaviside(t)+heaviside(t-pi);
f=pi/2*(sawtooth(t+pi,0.5)+1).*f1;
w1=2*pi*5;
N=200;
k=-N:N;
w=k*w1/N;
F=r*f*exp(-j*t'*w);
F2=r*f2*exp(-j*t'*w)
F3=F2./(j*w);
subplot(411);
plot(t,f2);
set(gca,'box','off')
xlabel('t');
ylabel('f2(t)');
subplot(412);
plot(t,f);
set(gca,'box','off')
xlabel('t');
ylabel('f(t)');
subplot(413);
plot(w,F);
set(gca,'box','off')
xlabel('w');
ylabel('F(jw)');
subplot(414);
plot(w,F3);
set(gca,'box','off')
xlabel('w');
ylabel('F3(jw)');
clear all;
clc;

