clear all;
close all;
clc;
syms t w;
x1=heaviside(t+1)-heaviside(t-1);
x2=heaviside(t+2)-heaviside(t-2);
x=x1+x2;
X1=simple(fourier(x1,t,w));
subplot(2,2,1);
ezplot(X1);
grid on;
X2=simple(fourier(x2,t,w));
subplot(2,2,2);
ezplot(X2);
grid on;
X=simple(fourier(x,t,w));
subplot(2,2,3);
ezplot(X);
title('X');
grid on;
subplot(2,2,4);
ezplot(X1+X2);
title('X1+X2');
grid on;
